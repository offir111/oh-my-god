/** מחזיר וירטואלי אקראי מהצד הנגדי */
export function getRandomVirtualOpponent(userSide) {
  const oppositeSide = userSide === 'believer' ? 'atheist' : 'believer';
  const candidates = VIRTUAL_USERS.filter(u => u.side === oppositeSide);
  return candidates[Math.floor(Math.random() * candidates.length)];
}

/** מחזיר וירטואלי לפי שם */
export function getVirtualUserByUsername(username) {
  const norm = String(username || '').trim().toLowerCase();
  return VIRTUAL_USERS.find(u => u.username.toLowerCase() === norm) || null;
}
